export function systemPrompt(domain: 'astro'|'numerology'|'tarot') {
  const base = 'Ты отвечаешь только в рамках заданной дисциплины, кратко и по делу. Если вопрос вне домена — откажись.';
  
  if (domain === 'astro') {
    return base + ' Домен: астрология (натальная карта, транзиты, дома, аспекты, совместимость, соляр/лунар). Запрещено: финансы, медицина, правовые и бытовые темы.';
  }
  
  if (domain === 'numerology') {
    return base + ' Домен: нумерология (числа судьбы/души, матрицы, Пифагор). Запрещено всё вне домена.';
  }
  
  return base + ' Домен: таро (расклады, арканы, значения). Без эзотерики вне колоды.';
}